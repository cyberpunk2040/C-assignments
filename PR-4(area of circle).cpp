// Reads the radius of the circle. calculate area and circumference.


  #include<iostream>
  #define PI 3.14
  using namespace std;
  int main()
  {

       float radius;

      cout<<"Enter the radius of circle: ";
      cin>>radius;

      // calculation of area and circumference.

      cout<<"\nThe Area of circle is :"<<PI*radius*radius<<endl;
      cout<<"The Circumference of circle is :"<<2*PI*radius;

        return 0;
  }





